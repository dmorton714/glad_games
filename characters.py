import os

class Character:
    def __init__(self, name, art, lore, stats):
        self.name = name
        self.art = art
        self.lore = lore
        self.stats = stats

    def display(self):
        print(self.art)
        print(self.lore)
        print("Stats:")
        for key, value in self.stats.items():
            print(f"{key}: {value}")
        input("Press Enter to see the next class")
        self.clear_screen()

    @staticmethod
    def clear_screen():
        os.system('cls' if os.name == 'nt' else 'clear')

